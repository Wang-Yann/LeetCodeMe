select u.user_id as buyer_id, u.join_date, ifnull(os.cnt, 0) as orders_in_2019
from Users as u
         left join (
    select buyer_id, count(order_id) as cnt from Orders where EXTRACT(year from order_date) = 2019 group by buyer_id
) as os on u.user_id = os.buyer_id;


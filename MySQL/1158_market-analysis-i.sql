-- #Users table:
-- #+---------+------------+----------------+
-- #| user_id | join_date  | favorite_brand |
-- #+---------+------------+----------------+
-- #| 1       | 2018-01-01 | Lenovo         |
-- #| 2       | 2018-02-09 | Samsung        |
-- #| 3       | 2018-01-19 | LG             |
-- #| 4       | 2018-05-21 | HP             |
-- #+---------+------------+----------------+

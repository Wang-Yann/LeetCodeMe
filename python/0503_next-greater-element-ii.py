    # [[2, -1, 2], [4, -1, 4, 4, 4],[120,11,120,120,123,123,-1,100,100,100]]
    samples = [
        [1, 2, 1],
        [1, 4, 3, 2, 1],
        [100, 1, 11, 1, 120, 111, 123, 1, -1, -100]
    ]
    lists = [x for x in samples]
    res = [sol.nextGreaterElements(x) for x in lists]

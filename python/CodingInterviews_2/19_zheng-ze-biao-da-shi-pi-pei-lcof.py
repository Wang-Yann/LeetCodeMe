import pytest


class Solution:

    def isMatch(self, s: str, p: str) -> bool:
        """
        å½“*å‡ºçŽ°æ—¶ï¼Œä¸€å®šä¼šåœ¨å‰é¢è·Ÿä¸€ä¸ªå…¶ä»–å­—ç¬¦ï¼Œæ‰€ä»¥ä¸€å®šä¼šå‡ºçŽ°åœ¨pattern[1]çš„ä½ç½®ã€‚ä¸€ç§æƒ…å†µæ˜¯æˆ‘ä»¬å¿½ç•¥è¿™å¯¹patternï¼Œå› ä¸ºå¯ä»¥å‡ºçŽ°0æ¬¡ï¼›
        å¦ä¸€ç§æƒ…å†µæ˜¯åŒ¹é…ä¸Šè¿™ä¸ªå­—ç¬¦ï¼Œç”¨é€’å½’çš„æ–¹å¼åŒ¹é…ä¸‹ä¸€ä¸ªã€‚

         ä¸€å®š  è¦ç”¨f_match = bool(s)ï¼Œå¦åˆ™ç»“æžœå¯èƒ½è¾“å‡º''
        """
        if not p:
            return not s
        f_match = bool(s) and p[0] in [s[0], "."]
        if len(p) > 1 and p[1] == "*":
            return self.isMatch(s, p[2:]) or (
                    f_match and self.isMatch(s[1:], p)
            )
        else:
            return f_match and self.isMatch(s[1:], p[1:])


@pytest.mark.parametrize("kwargs,expected", [
    (dict(s="aa", p="a"), False),
    (dict(s="aa", p="a*"), True),
    (dict(s="ab", p=".*"), True),
    (dict(s="aab", p="c*a*b"), True),
    (dict(s="mississippi", p="mis*is*p*."), False),
    (dict(s="ab", p=".*c"), False),
])
def test_solutions(kwargs, expected):
    assert Solution().isMatch(**kwargs) == expected


if __name__ == '__main__':
    pytest.main(["-q", "--color=yes", "--capture=no", __file__])

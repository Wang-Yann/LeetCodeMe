# leetcode submit region begin(Prohibit modification and deletion)
class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        N = 256
        CNT = sum(count)
        mi = next(i for i in range(N) if count[i]) * 1.0
        ma = next(i for i in range(N - 1, -1, -1) if count[i]) * 1.0
        mean = sum(i * v for i, v in enumerate(count)) * 1.0 / CNT
        mode = count.index(max(count)) * 1.0
        for i in range(N - 1):
            count[i + 1] += count[i]
        median1 = bisect.bisect_left(count, (CNT + 1) // 2)
        median2 = bisect.bisect_left(count, (CNT + 2) // 2)
        # print(count,median1,median2)

        median = (median1 + median2) / 2.0
        return [mi, ma, mean, median, mode]


